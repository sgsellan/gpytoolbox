class TestDecD0(unittest.TestCase):

    def test_triangle(self):
        f = np.array([[0,1,2]],dtype=int)

        d0 = gpy.dec_d0(f)

        gt_arr = np.array([[1., -1., 0.], [-1., 0., 1.], [0., 1., -1.]])
        self.assertTrue(np.isclose(d0.todense(), gt_arr).all())

    def test_constant_kernel(self):
        v,f = gpy.read_mesh("test/unit_tests_data/bunny_oded.obj")

        d0 = gpy.dec_d0(f)

        self.assertTrue(np.isclose(d0*np.ones(v.shape[0]),
            np.zeros(d0.shape[0])).all())

